int	ft_sqrt(int nb)
{
	long int	i;

	i = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb >= 2)
	{
		while (i * i <= nb && i <= nb / 2)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
